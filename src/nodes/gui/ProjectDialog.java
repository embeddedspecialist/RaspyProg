    private void clearTable(JTable table){
        //Pulisco la tabella
        int nOfRows = ((DefaultTableModel) table.getModel()).getRowCount();
        if (nOfRows > 0) {
            for (int row = 0; row < nOfRows; row++) {
                ((DefaultTableModel) table.getModel()).removeRow(0);
            }
        }
    }

