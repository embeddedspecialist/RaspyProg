    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        if (!txtSerialNumber.getText().isEmpty()){
            if ( FieldChecker.checkSerialNumber(deviceName, txtSerialNumber.getText()) &&
                 FieldChecker.checkValidAddress(txtAddress.getText())
               ) {
                returnStatus = RET_OK;
                setVisible(false);
            }
            else {
                return;
            }
        }

        returnStatus = RET_OK;
        setVisible(false);
    }//GEN-LAST:event_btnOkActionPerformed

